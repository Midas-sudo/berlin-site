///Primeiro Dia

$(document).ready(function() {
  $(".rotateinfo1-1").click(function() {
    document
      .getElementById("1dia1")
      .setAttribute(
        "style",
        "animation:spin 2s linear; border:4px solid #fff;"
      );
    setTimeout(function() {
      document.getElementById("front1-1").style.display = "none";
    }, 500);
    setTimeout(function() {
      document.getElementById("back1-1").style.display = "inherit";
    }, 500);
    setTimeout(function() {
      document.getElementById("back1-1").style.transform = "rotateX(0deg)";
      document.getElementById("front1-1").style.transform = "rotateX(180deg)";
    }, 998);
    setTimeout(function() {
      document.getElementById("1dia1").style.border = "none";
      document
        .getElementById("1dia1")
        .setAttribute("style", "animation-play-state: paused");
    }, 1000);
  });
});

