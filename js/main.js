document.getElementById("theme_button").onclick = function() {
  if ((document.getElementById("theme_css").href = "css/theme1.css")) {
    document.getElementById("theme_css").href = "css/theme2.css";
  } else if ((document.getElementById("theme_css").href = "css/theme2.css")) {
    document.getElementById("theme_css").href = "css/theme1.css";
  }
};
