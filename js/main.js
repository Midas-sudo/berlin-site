$(document).ready(function() {
  $(".dia1.buttoesR").click(function() {
    $(".all").fadeToggle();
    setTimeout(function() {
      $(".info").toggle();
      $(".dia1").toggle();
      $(".theme").toggle();
      $(".dia2.buttoesR").toggle();
    }, 600);
    setTimeout(function() {
      document.getElementById("showcase").style.background =
        "url('./img/main1.jpeg') no-repeat center/cover";
      document.getElementById("barra").style.textAlign = "center";
    }, 600);
    setTimeout(function() {
      $(".all").fadeToggle();
    }, 750);
  });
});

